/**
 * Build a single block-editor paragraph. Wrapping the markup in the core block
 * comments keeps the imported content editable as native Gutenberg blocks
 * rather than a "Classic" blob.
 */
function paragraph(text: string): string {
  return `<!-- wp:paragraph -->\n<p>${escapeHtml(text)}</p>\n<!-- /wp:paragraph -->`;
}

function heading(text: string): string {
  return `<!-- wp:heading -->\n<h2 class="wp-block-heading">${escapeHtml(text)}</h2>\n<!-- /wp:heading -->`;
}

function list(items: string[]): string {
  const inner = items
    .map(
      (item) =>
        `<!-- wp:list-item -->\n<li>${escapeHtml(item)}</li>\n<!-- /wp:list-item -->`,
    )
    .join('\n');
  return `<!-- wp:list -->\n<ul class="wp-block-list">\n${inner}\n</ul>\n<!-- /wp:list -->`;
}

/** Compose several block fragments into one `<content:encoded>` payload. */
function blocks(...fragments: string[]): string {
  return fragments.join('\n\n');
}

/** Escape the five XML-significant characters for use in element text. */
function escapeHtml(value: string): string {
  return value
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#039;');
}

/** Wrap a value in a CDATA section, neutralising any nested terminators. */
function cdata(value: string): string {
  return `<![CDATA[${value.replace(/]]>/g, ']]]]><![CDATA[>')}]]>`;
}

/**
 * WordPress stores two timestamps per post: site-local and GMT. The demo
 * content uses UTC, so both columns share the same literal value (just
 * reformatted from ISO `T` notation to the space-separated SQL form).
 */
function sqlDate(date: string): string {
  return date.replace('T', ' ');
}

const WEEKDAYS = ['Sun', 'Mon', 'Tue', 'Wed', 'Thu', 'Fri', 'Sat'] as const;
const MONTHS = [
  'Jan',
  'Feb',
  'Mar',
  'Apr',
  'May',
  'Jun',
  'Jul',
  'Aug',
  'Sep',
  'Oct',
  'Nov',
  'Dec',
] as const;

/** Convert an ISO-ish literal to the RFC-822 `<pubDate>` WordPress emits. */
function pubDate(date: string): string {
  const datePart = date.split('T')[0] ?? '';
  const timePart = date.split('T')[1] ?? '00:00:00';
  const parts = datePart.split('-');
  const year = Number.parseInt(parts[0] ?? '0', 10);
  const month = Number.parseInt(parts[1] ?? '1', 10);
  const day = Number.parseInt(parts[2] ?? '1', 10);

  // Zeller-style weekday calculation; deterministic and dependency-free.
  const m = month < 3 ? month + 12 : month;
  const y = month < 3 ? year - 1 : year;
  const k = y % 100;
  const j = Math.floor(y / 100);
  const h =
    (day +
      Math.floor((13 * (m + 1)) / 5) +
      k +
      Math.floor(k / 4) +
      Math.floor(j / 4) +
      5 * j) %
    7;
  const weekday = WEEKDAYS[(h + 6) % 7];
  const monthName = MONTHS[month - 1];
  return `${weekday}, ${String(day).padStart(2, '0')} ${monthName} ${year} ${timePart} +0000`;
}

function renderItem(item: SeedItem): string {
  const link = `${SITE_LINK}/?p=${item.id}`;
  return [
    '\t\t<item>',
    `\t\t\t<title>${escapeHtml(item.title)}</title>`,
    `\t\t\t<link>${link}</link>`,
    `\t\t\t<pubDate>${pubDate(item.date)}</pubDate>`,
    `\t\t\t<dc:creator>${cdata(AUTHOR_LOGIN)}</dc:creator>`,
    `\t\t\t<guid isPermaLink="false">${link}</guid>`,
    '\t\t\t<description></description>',
    `\t\t\t<content:encoded>${cdata(item.content)}</content:encoded>`,
    `\t\t\t<excerpt:encoded>${cdata(item.excerpt)}</excerpt:encoded>`,
    `\t\t\t<wp:post_id>${item.id}</wp:post_id>`,
    `\t\t\t<wp:post_date>${cdata(sqlDate(item.date))}</wp:post_date>`,
    `\t\t\t<wp:post_date_gmt>${cdata(sqlDate(item.date))}</wp:post_date_gmt>`,
    `\t\t\t<wp:comment_status>${cdata('open')}</wp:comment_status>`,
    `\t\t\t<wp:ping_status>${cdata('open')}</wp:ping_status>`,
    `\t\t\t<wp:post_name>${cdata(item.slug)}</wp:post_name>`,
    `\t\t\t<wp:status>${cdata('publish')}</wp:status>`,
    `\t\t\t<wp:post_parent>0</wp:post_parent>`,
    `\t\t\t<wp:menu_order>${item.id}</wp:menu_order>`,
    `\t\t\t<wp:post_type>${cdata(item.postType)}</wp:post_type>`,
    `\t\t\t<wp:post_password>${cdata('')}</wp:post_password>`,
    `\t\t\t<wp:is_sticky>0</wp:is_sticky>`,
    '\t\t</item>',
  ].join('\n');
}

