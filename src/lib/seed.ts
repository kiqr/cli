const PRESET_ITEMS: Record<SeedPreset, () => SeedItem[]> = {
  blog: blogItems,
  portfolio: portfolioItems,
  woocommerce: woocommerceItems,
};

/**
 * Generate a complete, valid WXR 1.2 document for the given preset.
 *
 * Pure and deterministic: calling it twice with the same preset returns
 * byte-for-byte identical output.
 */
export function generateSeedWxr(preset: SeedPreset): string {
  const items = PRESET_ITEMS[preset]();
  const itemsXml = items.map(renderItem).join('\n');

  return `<?xml version="1.0" encoding="UTF-8" ?>
<!-- This is a WordPress eXtended RSS file generated by the Kiqr CLI. -->
<!-- Preset: ${preset}. Import it with: kiqr wp import kiqr-seed-${preset}.xml --authors=create -->
<rss version="2.0"
\txmlns:excerpt="http://wordpress.org/export/1.2/excerpt/"
\txmlns:content="http://purl.org/rss/1.0/modules/content/"
\txmlns:wfw="http://wellformedweb.org/CommentAPI/"
\txmlns:dc="http://purl.org/dc/elements/1.1/"
\txmlns:wp="http://wordpress.org/export/1.2/"
>
\t<channel>
\t\t<title>${escapeHtml(SITE_TITLE)}</title>
\t\t<link>${SITE_LINK}</link>
\t\t<description>${escapeHtml(SITE_DESCRIPTION)}</description>
\t\t<pubDate>${pubDate('2024-01-01T00:00:00')}</pubDate>
\t\t<language>en-US</language>
\t\t<wp:wxr_version>1.2</wp:wxr_version>
\t\t<wp:base_site_url>${SITE_LINK}</wp:base_site_url>
\t\t<wp:base_blog_url>${SITE_LINK}</wp:base_blog_url>
\t\t<wp:author>
\t\t\t<wp:author_id>1</wp:author_id>
\t\t\t<wp:author_login>${cdata(AUTHOR_LOGIN)}</wp:author_login>
\t\t\t<wp:author_email>${cdata(AUTHOR_EMAIL)}</wp:author_email>
\t\t\t<wp:author_display_name>${cdata(AUTHOR_NAME)}</wp:author_display_name>
\t\t\t<wp:author_first_name>${cdata('')}</wp:author_first_name>
\t\t\t<wp:author_last_name>${cdata('')}</wp:author_last_name>
\t\t</wp:author>
\t\t<generator>https://github.com/kiqr/cli</generator>
${itemsXml}
\t</channel>
</rss>
`;
}
