/**
 * Pure, deterministic generator for WordPress demo content.
 *
 * The output is a WXR (WordPress eXtended RSS) 1.2 document that can be fed
 * straight into `wp import`. Everything here is intentionally free of any
 * time- or randomness-based APIs: every date is a fixed literal so that the
 * exact same bytes are produced on every run (which keeps it diff-friendly and
 * unit-testable).
 */

export const SEED_PRESETS = ['blog', 'portfolio', 'woocommerce'] as const;

export type SeedPreset = (typeof SEED_PRESETS)[number];

/** A single content node that becomes one `<item>` in the WXR feed. */
type SeedItem = {
  /** Sequential id; also used for the post id and menu order. */
  id: number;
  title: string;
  /** URL slug, used for `<wp:post_name>` and the guid. */
  slug: string;
  /** WordPress post type: post, page, product, etc. */
  postType: string;
  /** Full block-editor markup for `<content:encoded>`. */
  content: string;
  /** Short excerpt for `<excerpt:encoded>`. */
  excerpt: string;
  /** Literal date string, e.g. '2024-01-15T10:00:00' (no timezone, no `Z`). */
  date: string;
};

